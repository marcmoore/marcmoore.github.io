$(document).ready(function(){
	$('body').jpreLoader({
		showSplas: false,
		autoClose: true,
		showPercentage: true,
		closeBtnText: ""
	}, function() {	//callback function
		$(".pageLoading").fadeOut(300);
	});
	var animating = false;
	
	var sectionHide = function (hideThis){
		if(animating) return false;
		animating = true;
		
		$(hideThis).animate({opacity: 0}, {
			duration: 1000,
			complete: function(){
				animating = false;
				$(hideThis).css({"z-index": -100});
			},
			easing: 'easeInOutBack'
		});
	};
	
	var sectionShow = function (showThis){
		if(animating) return false;
		animating = true;
		
		$(showThis).css({"z-index": 1000});
		$(showThis).animate({opacity: 1}, {
			step: function(now, mx) {
				var opacity = 1 - now;
				var scale = 1 - (1 - now) * 0.2;
				$(showThis).css({"opacity": opacity});
				$(showThis).css({"transform": "scale("+scale+")"});
			},
			duration: 800,
			complete: function(){
				animating = false;
			},
			easing: "easeInOutBack"
		});
	};
	
	$(".logo").on("click", function(){
		sectionHide(".splashScreen");
	});
	
	$(".schools").on("click", function(){
		var whichSchool = $(this).attr("name");
		$(".schoolIntro img").attr("src", schools[whichSchool].img);
		
		var qandaContent = "";
		$(schools[whichSchool].qanda).each(function(){
			qandaContent = qandaContent + "<li>" + this.q + "</li>" + "<li>" + this.a + "</li>";
		});
		
		$(".qanda").html(qandaContent);
		
		sectionShow(".schoolIntro");
	});
	
	$(".logoTiny").on("click", function(){		
		sectionShow(".ufIntro");
	});
	
	$(".aboutme").on("click", function(){
		sectionShow(".earlyBird");
	});
	
	$(".closeBtn").on("click", function(){
		sectionHide(".panel");
	});
	
	$(".start").on("click", function(){
		if(animating) return false;
		animating = true;
		$(".helicopter").addClass("move");
		$(".helicopter span").css("opacity", "1");
		var popup = $(".map");
		var start = $(".startContainer");
		popup.animate({opacity: 1}, {
			step: function(now, mx) {
				var opacity = 1 - now;
				var scale = 1 - (1 - now) * 0.2;
				var bottom = now * -60 + "px";
				
				popup.css({"transform": "scale("+scale+")"});
				popup.css({"opacity": opacity});
				start.css({"bottom": bottom});
			},
			duration: 800,
			complete: function(){
				animating = false;
			},
			easing: "easeInOutBack"
		});
		
		$(".helicopter").on("click", function(){
			sectionShow(".share");
		});
	});
	
	var speed = 10;
	var schoolBg = $(".schoolBg");

	function schoolRoll(){
		if(schoolBg.scrollLeft() >= 302)
			schoolBg.scrollLeft(0);
		else{
			schoolBg.scrollLeft(schoolBg.scrollLeft()+1);
		}
	}
	var sliding = setInterval(schoolRoll,speed);
});
